class cookman(Thread):
    username = ""
    count = 0
    def run(self)-> None :
        while time.perf_counter() <= 40:
            global saveegg,savemoney
            if saveegg == 500 :
                for i in range(1):
                    print(self.username,"给爷冷静三秒",end="")
                    time.sleep(3)
            else:
                saveegg += 1
                self.count += 1
                savemoney += 3
                print(self.username,"来一个蛋")
        money = self.count *2.5
        print(self.username,"今天赚了",money)

